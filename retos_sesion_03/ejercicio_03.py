# Tutorial detallado: Cómo lavar los platos correctamente 🍽️

print("🔹 Paso 1: Preparar los materiales")
print("   - Esponja suave para platos delicados.")
print("   - Esponja de fibra gruesa para ollas y sartenes.")
print("   - Jabón para platos (preferiblemente biodegradable).")
print("   - Agua caliente o tibia para una limpieza más efectiva.")
print("   - Un escurridor o toalla limpia para secar los platos.")

print("\n🔹 Paso 2: Separar los platos por tipo")
print("   - Vasos y tazas.")
print("   - Platos y utensilios.")
print("   - Ollas y sartenes.")

print("\n🔹 Paso 3: Enjuagar los platos antes de lavar")
print("   - Quita los restos grandes de comida con agua.")
print("   - Si hay comida pegada, deja los platos en remojo unos minutos.")

print("\n🔹 Paso 4: Aplicar jabón y frotar con la esponja")
print("   - Usa la esponja adecuada para cada tipo de plato.")
print("   - Lava primero los objetos menos sucios (vasos, cubiertos).")
print("   - Luego lava los platos y utensilios.")
print("   - Al final, limpia las ollas y sartenes para evitar que la grasa manche otros platos.")

print("\n🔹 Paso 5: Enjuagar bien con agua limpia")
print("   - Asegúrate de eliminar completamente el jabón.")
print("   - Usa agua caliente para mejorar la limpieza.")

print("\n🔹 Paso 6: Secar los platos")
print("   - Usa un escurridor para que el agua escurra naturalmente.")
print("   - Si prefieres secar a mano, usa un paño limpio y seco.")
print("   - Guarda los platos en su lugar adecuado para mantener la organización.")